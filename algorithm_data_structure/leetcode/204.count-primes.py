# @lc code=start
class Solution:
    def countPrimes(self, n: int) -> int:
        is_prime = [True] * n
        for i in range(2, int(n ** 0.5) + 1):
            if is_prime[i]:
                for j in range(i * i, n, i):
                    is_prime[j] = False
        return len([x for x in range(2, n, 1) if is_prime[x]])


